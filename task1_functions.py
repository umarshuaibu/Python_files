#SIMPLE CALCULATOR FUNCTIONS
#Addition
def addition(num1,num2):
    return num1 + num2

#Subtraction
def subtraction(num1,num2):
    return num1 - num2

#Multiplication
def multiplication(num1,num2):
    return num1 * num2

#Division
def Division(num1,num2):
    return num1 / num2

#CGPA
def cgpa(num1,num2):
    return (num1 + num2)/2

