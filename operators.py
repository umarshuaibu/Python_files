a = 10
b = 30

#Addition
c = a + b
print (f'{a} + {b} = {c} ')

#Subtraction
d = b - a
print(f'{b} - {a} = {d} ') 

#Multiplication
e = b * a
print(f'{b} * {a} = {e} ')

#Division
f = b/a
print(f'{b} / {a} = {f} ')

#Module
g = a % b
print(f'{a} % {b} = {g} ')

#Exponential or power
h = a ** b
print(f'{a} power {b} = {h} ')

